marks = int(input("Enter your marks to calculate grade\n"))

if marks>=90:
    print("Your Grade is EX")

elif marks>=80:
    print("Your Grade is A")

elif marks>=70:
    print("Your Grade is B")

elif marks>=60:
    print("Your Grade is C")

elif marks>=50:
    print("Your Grade is D")

else:
    print("Your Grade is F")
    
